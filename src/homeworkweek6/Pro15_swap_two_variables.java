package homeworkweek6;

public class Pro15_swap_two_variables {

    // Write a Java program to swap two variables.

    public static void main(String[] args) {

        int xx, yy, temp; // local variable
        xx = 100;
        yy = 200;
        System.out.println("Before swapping : xx, yy = "+ xx +"," + + yy);
        temp = xx;
        xx = yy;
        yy = temp;
        System.out.println("After swapping : xx, yy = "+ xx +"," + yy);

}

}

